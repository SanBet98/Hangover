                <h2><b>HANGOVER: LA RIVOLUZIONE DEL DIVERTIMENTO GIOVANILE</b></h2>
                <h4>Vi siete mai soffermati a riflettere sul grande cambiamento della mentalità giovanile?</h4>
Oggigiorno le nuove generazioni presentano una precoce ed ostentata ricerca di un divertimento sempre più adulto, che si riflette in un costante aumento delle presenze giovanili nel mondo della vita notturna bresciana: questo processo è cresciuto esponenzialmente grazie all’avvento degli smartphone e dei social network, che anno dopo anno accolgono i giovanissimi e li interfacciano con il grande mondo del web.
Analizzando attentamente questa situazione è emersa, nel nostro territorio, la carenza di fonti di divertimento per i più giovani: tutto questo ha fornito una spinta ulteriore alla nascita del progetto HANGOVER.<br>
HANGOVER è nato ad ottobre 2017 grazie alla passione e alla dedizione di cinque ragazzi: ci siamo messi in gioco e nel giro di poche settimane abbiamo formato una nuova organizzazione: nel frattempo erano cominciati i contatti e le telefonate con i vari locali della città per trovare la location perfetta per i futuri eventi.
Nelle settimane che hanno preceduto il primo evento, abbiamo lavorato sfruttando al massimo ogni mezzo comunicativo al fine di portare il nome HANGOVER agli occhi di tutti i giovani Bresciani e soprattutto a tutti gli studenti degli istituti superiori cittadini attraverso mirati volantinaggi.
Le idee, le aspettative, le ansie e i timori si sono tramutati molto presto in realtà: in data 9 dicembre 2017 si è tenuto, presso il Social Club di Via della Ziziola, il primo evento targato HANGOVER.<br>
Il feedback è stato molto positivo: più di mille persone hanno risposto presenti alla chiamata del divertimento, permettendo così al progetto di proseguire ed evolversi sempre più.<br>
Galvanizzati dall’incredibile risultato, abbiamo portato avanti l’ambizioso progetto: pochi mesi dopo, esattamente il 3 marzo 2018, si è tenuto il secondo evento targato HANGOVER, che ha superato tutte le più rosee aspettative.
Mai, prima d’allora, un evento studentesco aveva toccato quei numeri: più di millecinquecento studenti presenti, accomunati dalla voglia di staccare dalle ansie e dalle preoccupazioni della solita routine per divertirsi liberamente in quella magica serata.
HANGOVER ha raggiunto molto velocemente gli occhi e i cuori dei giovani bresciani: molto è dovuto alla pagina instagram ufficiale @hangover.eventi che giornalmente condivide post, foto e video riguardanti gli eventi passati per mostrarsi sempre presente agli occhi di tutti. 
Il 19 maggio 2018 si è tenuto il terzo evento targato HANGOVER, che ha permesso di chiudere la prima stagione nel migliore dei modi: anche in questa occasione più di milleduecento studenti non si son fatti sfuggire la possibilità di vivere ancora una volta una serata unica nel suo genere, impreziosita dall’esibizione live del duo bresciano “Pretty Saga”, divenuto virale fra i giovani grazie a singoli come “Balenciaga”.
L’acquisita notorietà ci ha permesso di collaborare direttamente con uno degli eventi più famosi di tutta Italia, “School is Over”, giunto alla decima edizione.<br>
Abbiamo infatti avuto la possibilità di gestire uno degli stage più importanti: ancora una volta, grazie anche allo schiuma party, il risultato è stato sorprendente ed ha sancito la definitiva affermazione della nostra organizzazione come una delle più solide a livello bresciano.
Negli anni successivi abbiamo continuato ad organizzare evento dopo evento, raggiungendo definitivamente una posizione d'elite all'interno del panorama notturno bresciano: questa notorietà ci ha permesso di ospitare, in una delle nostre serate, la DJ e influencer Ludovica Pagani, che conta quasi 3 milioni di follower su instagram. <br>
Questa macchina perfetta è andata avanti fino alla fine del 2019: purtroppo la nostra avventura ha subito una brusca frenata con l'arrivo della pandemia che ancora oggi stiamo affrontando. <br>
<br>
"Del doman non v'è certezza": solo il tempo potrà dire quando tornereremo a divertirci e a ballare tutti insieme a ritmo di HANGOVER. <br>
